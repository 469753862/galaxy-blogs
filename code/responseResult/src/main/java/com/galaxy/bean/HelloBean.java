package com.galaxy.bean;

public class HelloBean {

    String name;
    Integer age;

    public HelloBean() {
    }

    public HelloBean(String name, Integer age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }
}
